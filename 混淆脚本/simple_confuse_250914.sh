backupFile() {
    local file=$1
    local fileName=${file##*/}
    local backupPath=${file/$fileName/.$fileName$BACKUP_EXTENSION}
    cp "$file" "$backupPath"
    echo "$backupPath" >> $BACKUP_FILE
