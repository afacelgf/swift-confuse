randomString() {
    openssl rand -base64 64 | tr -cd 'a-zA-Z' | head -c 16
}

removeIfExist() {
    if [ -f "$1" ]; then rm "$1"; fi
}

ignore_path_args() {
    local args=""
    for dir in "${IGNORE_DIRS[@]}"; do
